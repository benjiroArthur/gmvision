    {
        $activityVideo = new ActivityVideo();
        DB::beginTransaction();
        try {
            $activityVideo->create([
                "title" => $request->title,
                "url" => $request->url,
                "description" => $request->description
            ]);
            DB::commit();
            $message = 'success';
            return response()->json(["message" => $message]);
        } catch (\Exception $exception){
            DB::rollBack();
            $message = 'Something went wrong, please try again';
            return response()->json(["message" => $message]);
        }
    }

